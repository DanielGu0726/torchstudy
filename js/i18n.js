  function getLang() {
    return localStorage.getItem(LANG_KEY) || 'ko';
  }

  function applyLang(lang) {
    const dict = T[lang] || T.ko;

    // data-i18n â†’ textContent
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (dict[key] !== undefined) el.textContent = dict[key];
    });

    // data-i18n-html â†’ innerHTML (for text with <br>, <strong>, <a>)
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const key = el.getAttribute('data-i18n-html');
      if (dict[key] !== undefined) el.innerHTML = dict[key];
    });

    // data-i18n-placeholder â†’ placeholder attribute
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      if (dict[key] !== undefined) el.placeholder = dict[key];
    });

    // Update toggle button active state
    const btnKo = document.getElementById('btn-ko');
    const btnEn = document.getElementById('btn-en');
    if (btnKo) btnKo.classList.toggle('active', lang === 'ko');
    if (btnEn) btnEn.classList.toggle('active', lang === 'en');

    // Update html lang attribute
    document.documentElement.lang = lang === 'en' ? 'en' : 'ko';
  }

  function setLang(lang) {
    localStorage.setItem(LANG_KEY, lang);
    applyLang(lang);
  }

  document.addEventListener('DOMContentLoaded', () => {
    applyLang(getLang());
  });

  return { setLang, getLang, applyLang };
